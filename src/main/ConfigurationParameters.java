package main;

import interfaces.ConfigurationInterface;

/**
 *
 * @author luis
 */
public class ConfigurationParameters implements ConfigurationInterface {

    private String fileName;
    private String filePath;
    private int craftsmanThreshold;
    private int amountProductsToCarryToWorkshop;
    private int amountPrimeMaterialsToReplenish;
    private int amountCraftsman;
    private int amountCustomers;
    private int amountProductsManufacturedPerPrimeMaterial;
    private int amountPrimeMaterialsTakenToTable;
    private int elapsedTime;
    private int stockPrimeMaterials;

    public ConfigurationParameters(String fileName, String filePath, int craftsmanThreshold,
            int amountProductsToCarryToWorkshop, int amountPrimeMaterialsToReplenish,
            int amountCraftsman, int amountCustomers, int amountProductsManufacturedPerPrimeMaterial,
            int amountPrimeMaterialsTakenToTable, int stockPrimeMaterials, int elapsedTime) {
        this.fileName = fileName;
        this.filePath = filePath;
        this.craftsmanThreshold = craftsmanThreshold;
        this.amountProductsToCarryToWorkshop = amountProductsToCarryToWorkshop;
        this.amountPrimeMaterialsToReplenish = amountPrimeMaterialsToReplenish;
        this.amountCraftsman = amountCraftsman;
        this.amountCustomers = amountCustomers;
        this.amountProductsManufacturedPerPrimeMaterial = amountProductsManufacturedPerPrimeMaterial;
        this.amountPrimeMaterialsTakenToTable = amountPrimeMaterialsTakenToTable;
        this.elapsedTime = elapsedTime;
        this.stockPrimeMaterials = stockPrimeMaterials;
    }

    @Override
    public String getFileName() {
        return fileName;
    }

    @Override
    public String getFilePath() {
        return filePath;
    }

    @Override
    public int getCraftsmanThreshold() {
        return craftsmanThreshold;
    }

    @Override
    public int getAmountProductsToCarryToWorkshop() {
        return amountProductsToCarryToWorkshop;
    }

    @Override
    public int getAmountPrimeMaterialsToReplenish() {
        return amountPrimeMaterialsToReplenish;
    }

    @Override
    public int getAmountCraftsman() {
        return amountCraftsman;
    }

    @Override
    public int getAmountCustomers() {
        return amountCustomers;
    }

    @Override
    public int getAmountProductsManufacturedPerPrimeMaterial() {
        return amountProductsManufacturedPerPrimeMaterial;
    }

    @Override
    public int getAmountPrimeMaterialsTakenToTable() {
        return amountPrimeMaterialsTakenToTable;
    }

    @Override
    public int getElapsedTime() {
        return elapsedTime;
    }

    @Override
    public int getStockPrimeMaterials() {
        return stockPrimeMaterials;
    }
}
